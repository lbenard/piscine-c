int	match(char *s1, char *s2)
{
	while (*(s2 + 1) == '*' && *(s2) == '*')
		s2++;
	if (!*s1 && *s2 == '*')
	{
		while (*s2)
		{
			if (*s2 != '*')
				return (0);
			s2++;
		}
		return (1);
	}
	if (*s1 == *s2)
		return ((!*s1 && !*s2) ? 1 : match(s1 + 1, s2 + 1));
	else if (*s2 == '*')
		if (match(s1 + 1, s2 + 1) || match(s1 + 1, s2))
			return (1);
		else
			return (match(s1, s2 + 1));
	else if (*s1 != *s2 || (*s1 || *s2))
		return (0);
	return (0);
}
